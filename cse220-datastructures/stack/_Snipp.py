def p(ch):
    _prec = {
        "(": 8,
        "{": 8,
        "[": 8,
        ")": 8,
        "}": 8,
        "]": 8,
        "^": 7,
        "*": 6,
        "/": 6,
        "%": 6,
        "+": 4,
        "-": 4,
        "<": 3,
        ">": 3,
        ">=": 3,
        "<=": 3,
        "==": 2,
        "!=": 2,
        '&&': 1
    }
    if ch in _prec.keys():
        return _prec[ch]
    else:
        raise Exception(f"{ch} is not valid operator")


def opener(ch):
    data = {
        "(": ')',
        "{": '}',
        "[": ']',
        ")": '(',
        "}": '{',
        "]": '[',
    }
    return data[ch]


